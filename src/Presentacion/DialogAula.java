    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
       if(txtidAula.getText().equals("")){
           JOptionPane.showMessageDialog(this, "INGRESAR UN ID");
           txtidAula.requestFocus();
           return;
       }
       if(txtNumeroAula.getText().equals("")){
           JOptionPane.showMessageDialog(this, "INGRESAR EL NUMERO DE AULA");
           txtNumeroAula.requestFocus();
           return;
       }
       if(txtCapacidad.getText().equals("")){
           JOptionPane.showMessageDialog(this, "INGRESAR LA CAPACIDAD DEL AULA ");
           txtCapacidad.requestFocus();
       }
       
       String idAula;
       int numeroAula,capacidad;
       idAula = txtidAula.getText();
       numeroAula = Integer.parseInt(txtNumeroAula.getText());
       capacidad = Integer.parseInt(txtCapacidad.getText());
       Aula x = new Aula(idAula,numeroAula,capacidad);
       if(reg){
            try{
               p.insertar(x);
                JOptionPane.showMessageDialog(this, "AULA REGISTRADA");
            }catch(SQLException e){
                JOptionPane.showMessageDialog(this, e.getMessage());
            } 
       }else{
           try{
                p.actualizar(x);
                JOptionPane.showMessageDialog(this, "AULA MODIFICADA");
            }catch(SQLException e){
                JOptionPane.showMessageDialog(this, e.getMessage());
            } 
       }
       txtbuscaraula.requestFocus();
       try {
             p.mostrarAula(modelo);
         } catch (SQLException ex) {
             JOptionPane.showMessageDialog(this, ex.getMessage());
         }
       limpiarTextos();
       textos(false);
       controles(true);
       grafico();
       
       
    }//GEN-LAST:event_btnRegistrarActionPerformed

