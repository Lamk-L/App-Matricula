    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        String idAula;
        idAula = JOptionPane.showInputDialog("INGRESAR ID DEL AULA A ELIMINAR: ");
        Aula aula;
        try{
            aula = AulaDAO.getInstancia().buscaAula(idAula);
            if(aula != null){
                txtidAula.setText(aula.getIdAula());
                txtNumeroAula.setText(""+aula.getNumeroAula());
                txtCapacidad.setText(""+aula.getCapacidad());
                int resp = JOptionPane.showConfirmDialog(this,"¿DESEAS ELIMINARLO","ELIMIAR DATO",JOptionPane.YES_NO_OPTION );
                if(resp == JOptionPane.YES_OPTION){
                    AulaDAO.getInstancia().eliminar(idAula);
                    JOptionPane.showMessageDialog(this,"REGISTRO ELIMINADO");
                }
                limpiarTextos();
                
            }else{
                JOptionPane.showMessageDialog(this,"EL ID DEL AULA NO EXISTE");
            }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }  
        txtbuscaraula.requestFocus();
        try {
             p.mostrarAula(modelo);
        } catch (SQLException ex) {
             JOptionPane.showMessageDialog(this, ex.getMessage());
        }
        grafico();
    }//GEN-LAST:event_btnEliminarActionPerformed

